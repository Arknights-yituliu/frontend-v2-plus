<template>
  <h1 id="elite-specialization-ranking-algorithm">精英化/专精排名算法</h1>
  <v-divider></v-divider>
  <p>
    精英化与专精性价比页面计算的是养成材料的理智开销排名。它不评价干员强度，也不把输出、生存、泛用性等战斗表现纳入排序。
  </p>

  <h2 id="data-source">数据来源</h2>
  <v-divider></v-divider>
  <ul>
    <li>干员基础信息来自 <v-code>operatorTable</v-code>。</li>
    <li>精英化、技能专精和模组材料来自 <v-code>/src/static/json/operator/operator_item_cost_table.json</v-code> 以及干员装备数据。</li>
    <li>材料理智价值来自当前用户配置下的物品价值表。</li>
    <li>精二率、专三率和开模组率来自干员练度调查缓存。</li>
  </ul>

  <h2 id="material-cost">材料开销计算</h2>
  <v-divider></v-divider>
  <p>页面先把每项养成材料转换为理智价值：</p>
  <pre><v-code>单项材料开销 = 物品理智价值 * 数量
单阶段材料开销 = Σ 单项材料开销
总材料开销 = Σ 单阶段材料开销</v-code></pre>
  <p>
    精英化、技能专精和模组升级都使用这一套汇总方式。龙门币、技巧概要、芯片、模组数据块、数据增补条、数据增补仪等固定消耗也会参与计算。
  </p>

  <h3 id="elite-cost">精英化</h3>
  <v-divider></v-divider>
  <ul>
    <li>精英化材料按 <v-code>rank1</v-code>、<v-code>rank2</v-code> 分阶段汇总。</li>
    <li>原始精英化材料数据中缺少龙门币时，页面会按星级补入精英化所需龙门币。</li>
    <li>阿米娅各升变形态统一使用术师阿米娅的精英化材料，避免重复计算。</li>
  </ul>

  <h3 id="skill-cost">技能专精</h3>
  <v-divider></v-divider>
  <ul>
    <li>每个技能分别计算专一、专二、专三材料开销。</li>
    <li>技能总材料开销为该技能三个专精阶段的开销总和。</li>
    <li>技能排名表会把筛选范围内所有技能展开后一起排序。</li>
  </ul>

  <h3 id="module-cost">模组</h3>
  <v-divider></v-divider>
  <ul>
    <li>每个模组分别计算 1 级、2 级、3 级材料开销。</li>
    <li>模组总材料开销为三个等级开销总和。</li>
    <li>表格中的“二级模组材料开销”展示为 1 级和 2 级累计开销，方便估算从不开模组到 2 级所需成本。</li>
  </ul>

  <h2 id="ranking">排名规则</h2>
  <v-divider></v-divider>
  <p>页面的默认排序方向都是材料开销从高到低。</p>
  <ul>
    <li>干员排名表：按干员精英化总材料开销降序排列。</li>
    <li>技能排名表：把筛选后的干员技能展开，按技能专精总材料开销降序排列。</li>
    <li>模组排名表：把筛选后的干员模组展开，按模组总材料开销降序排列。</li>
  </ul>
  <p>
    表格左侧的“排名”会随星级、职业、搜索关键词等筛选条件重新编号，因此它表示当前筛选结果内的排序位置。
  </p>

  <h2 id="detail-ranking">详情弹窗中的同星级排名</h2>
  <v-divider></v-divider>
  <p>
    点击某一行打开“干员分析”时，页面会改用同星级集合计算位置。对于 4 星及以上干员，页面分别收集同星级下所有干员的精英化开销、所有技能的专精开销、所有模组的模组开销，并按开销降序排列。
  </p>
  <pre><v-code>位置 = 同星级开销列表中第一个等于当前开销的位置 + 1
总数 = 同星级对应项目数量
位于 = (位置 - 1) / (总数 - 1) * 100%</v-code></pre>
  <p>
    如果多个项目材料开销相同，它们会共享该开销第一次出现的位置。
  </p>

  <h2 id="progression-rate">练度率展示</h2>
  <v-divider></v-divider>
  <p>
    精二率、专三率和开三级模组率来自练度调查数据。它们只作为参考列展示，不参与材料开销计算，也不影响默认排名。
  </p>

  <h2 id="custom-operator">自定义干员</h2>
  <v-divider></v-divider>
  <p>
    新建自定义干员时，页面会根据星级和职业生成固定消耗模板，例如龙门币、芯片、技巧概要、模组数据块等；用户补全可选材料后，页面把自定义干员插入同一套计算流程，并重新计算所有表格和详情排名。
  </p>

  <h2 id="notice">注意事项</h2>
  <v-divider></v-divider>
  <ul>
    <li>排名越靠前表示材料理智开销越高，不代表越值得培养。</li>
    <li>材料理智价值会随个人中心中的物品价值配置变化，因此排名也可能随配置改变。</li>
    <li>低星干员无法精二或缺少部分养成阶段时，页面只展示其可计算的项目。</li>
  </ul>
</template>
